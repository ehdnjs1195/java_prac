package chap02;

public class Datatype01 {

	public static void main(String[] args) {
		int a = 10;		//int형 변수 4Byte
		short s = 2;	//short형 변수 
		byte b = 6;		//byte형 변수
		long l = 123456789l;
		System.out.println(a);
		System.out.println(s);
		System.out.println(b);
		System.out.println(l);

	}

}
