package chap03;

public class Operator01 {

	public static void main(String[] args) {
		int a = 10;
		double b = 2.0;
		System.out.println("a + b = " + (a + b));	//정수형과 실수형을 연산하면 실수형으로 출력된다.
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b));
		System.out.println("a % b = " + (a % b));
	}

}
