package chap03;

public class Operator02 {

	public static void main(String[] args) {
		int a = 10, b = 10;
		System.out.println("a == b = " + (a == b));	//true
		System.out.println("a != b = " + (a != b));	//false
		System.out.println("a > b = " + (a > b));	//false
		System.out.println("a < b = " + (a < b));	//false
		System.out.println("a >= b = " + (a >= b));	//true
		System.out.println("a <= b = " + (a <= b));	//true
	}

}
